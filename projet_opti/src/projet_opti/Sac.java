package projet_opti;

public class Sac {

	private int valeur;
	private int poidsMax;
	
	public Sac() {
		super();
	}

	public int getValeur() {
		return valeur;
	}

	public void setValeur(int valeur) {
		this.valeur = valeur;
	}

	public int getPoidsMax() {
		return poidsMax;
	}

	public void setPoidsMax(int poidsMax) {
		this.poidsMax = poidsMax;
	}
	
	
}
